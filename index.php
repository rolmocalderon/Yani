            <div class="title">La importancia de la psicología</div>
            <div class="text">
                <p>Debido a la amplitud del campo de la psicología, su importancia depende en gran medida de la aplicación concreta a la que nos refiramos. No obstante, en este apartado reflexionaremos sobre la relevancia de la psicología a nivel general, haciendo hincapié en algunas de sus aportaciones clave.</p>

                <h3>1. Para entender a los seres vivos</h3>
                
                <p>Las teorías psicológicas son fundamentales para explicar la conducta de los animales, especialmente las personas: cualquier actividad producida en una sociedad humana es susceptible de ser analizada desde la psicología puesto que siempre implicará la participación de los procesos mentales básicos. En relación al estudio de los grupos humanos, la psicología social es particularmente importante.</p>
                
                <p>El análisis del comportamiento no sólo es interesante en sí mismo para un gran número de personas, sino que también permite aplicar los conocimientos adquiridos a muchos campos distintos, en especial dentro de las ciencias sociales y naturales como la medicina, la sociología, la economía, la historia, la ciencia política, la demografía, la lingüística, la veterinaria o la zoología.</p>
                

                <h3>2. Para aumentar la calidad de vida</h3>
                
                <p>Muchas personas consideran que el objetivo último de la psicología es aumentar el bienestar y la calidad de vida de los demás. Aunque otros profesionales tienen concepciones distintas, lo cierto es que el servicio a otras personas ha sido un aspecto central en el desarrollo de la psicología y sigue siéndolo a día de hoy.</p>
                
                <p>Evidentemente la psicología es útil para potenciar la salud mental y el crecimiento personal, pero los beneficios de esta ciencia no se limitan a la psique. La psicología de la salud, por ejemplo, procura el bienestar físico y la prevención de enfermedades a través de la adquisición de hábitos conductuales saludables, un aspecto clave que la medicina tiende a dejar de lado.</p>
                
        
                <h3>3. Para mejorar las relaciones</h3>
                
                <p>Los conocimientos derivados de la ciencia psicológica son muy importantes para la mejora de las relaciones humanas. Así, son los psicólogos quienes más capacitados están para mejorar las habilidades sociales de las personas tímidas, de mediar en casos de divorcio o de intervenir en el mobbing y el bullying, entre otras funciones.</p>
                

                <h3>4. Para potenciar el rendimiento profesional</h3>
                
                <p>En las sociedades actuales el rendimiento y la productividad son extremadamente valiosos en el plano profesional, y cada vez más personas se dan cuenta de que contar con psicólogos en su equipo de trabajo potencia los resultados de forma clara. Los recursos humanos, la psicología organizacional y la del deporte son buenos ejemplos de este rol.</p>
                
                
                <h3>5. Para desarrollar la cognición</h3>
                
                <p>Desde la psicología se han propuesto muchas técnicas para mejorar las capacidades cognitivas como el razonamiento lógico, la memoria o la solución de problemas. Hablamos de “metaconocimiento” para referirnos al conocimiento sobre los propios procesos de aprendizaje; entender cómo funcionan nuestros procesos mentales es muy importante para perfeccionarlos.</p>
                

                <h3>6. Para aumentar la creatividad</h3>
                
                <p>La psicología potencia la creatividad por distintos motivos. No sólo permite analizar el proceso artístico a nivel profundo, sino que la comprensión de la conducta y el funcionamiento mental de las personas es una ayuda muy importante para la creación de obras significativas y con personajes realistas.</p>
                

                <h3>7. Para solucionar problemas concretos</h3>
                
                <p>Tanto la psicología clínica como otras ramas de esta ciencia se han centrado frecuentemente en resolver los problemas cotidianos. En este sentido consideramos que merecen una mención especial las técnicas de resolución de problemas como las propuestas por D’Zurilla y Nezu, que determinan una serie de pasos útiles para afrontar cualquier situación compleja.</p>
                

                <h3>8. Para cambiar el mundo</h3>
                
                <p>Al fin y al cabo, muchas personas querríamos que la sociedad fuera más benévola con quienes la componen. Aunque las instituciones tienden a ignorar a la psicología por desconocimiento o por contraposición de intereses, si las aportaciones de nuestra ciencia en torno al bienestar individual y el funcionamiento colectivo se tuvieran más en cuenta el mundo sería sin duda un lugar mejor.</p>
                
